DECLARE
	CURSOR A2B_CURSOR
	IS SELECT RNO,NAME
	FROM A
	WHERE NAME LIKE 'A%';
	
	CURSOR B2A_CURSOR
	IS SELECT RNO,NAME
	FROM B
	WHERE NAME LIKE 'B%';
	
	
	F_RNO A.RNO%TYPE;
	F_NAME A.NAME%TYPE;
BEGIN
	OPEN A2B_CURSOR;
	LOOP
	FETCH A2B_CURSOR INTO F_RNO,F_NAME;
	EXIT WHEN A2B_CURSOR%NOTFOUND;
	INSERT INTO B
	VALUES(F_RNO,F_NAME);
	DELETE FROM A
	WHERE F_RNO=RNO;
	END LOOP;
	OPEN B2A_CURSOR;
	LOOP
	FETCH B2A_CURSOR INTO F_RNO,F_NAME;
	EXIT WHEN B2A_CURSOR%NOTFOUND;
	INSERT INTO A
	VALUES(F_RNO,F_NAME);
	DELETE FROM B
	WHERE F_RNO=RNO;
	END LOOP;
END;


	
