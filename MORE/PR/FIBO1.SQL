CREATE OR REPLACE PROCEDURE FIBO1(VAL1 IN NUMBER) IS
	
	A NUMBER(3);
	B NUMBER(3);
	C NUMBER(3);
BEGIN
	A:=0;
	B:=0;
	C:=1;
	WHILE(A<=VAL1)
	LOOP
	DBMS_OUTPUT.PUT_LINE(A);
	A:=B+C;
	C:=B;
	B:=A;
	END LOOP;
EXCEPTION
	WHEN OTHERS THEN
	DBMS_OUTPUT.PUT_LINE('AN EXCEPTION IS RAISED IN FIBO1 PROCEDURE');
END FIBO1;


