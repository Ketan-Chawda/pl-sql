CREATE OR REPLACE PROCEDURE FACTORIAL1(V1 IN NUMBER) IS
	
	F NUMBER(5);
BEGIN
	IF(V1=1) THEN
	F:=1;
	ELSE
	F:= V1*FACTORIAL(V1-1);
	END IF;
	DBMS_OUTPUT.PUT_LINE('FACTORIAL IS='||F);
EXCEPTION
	WHEN OTHERS THEN
	DBMS_OUTPUT.PUT_LINE('AN EXCEPTION IS RAISED IN FACTORIAL1 PROCEDURE');
END FACTORIAL1;
