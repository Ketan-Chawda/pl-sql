DECLARE
	CURSOR P2_CURSOR
	IS SELECT ENO,ENAME,ESAL FROM EMP
	ORDER BY ESAL DESC ;

	F_ENO EMP.ENO%TYPE;
	F_ESAL EMP.ESAL%TYPE;
	F_ENAME EMP.ENAME%TYPE;
	C NUMBER(2);
	UPTO NUMBER(2);
BEGIN
	OPEN P2_CURSOR;

	IF(P2_CURSOR%ISOPEN) THEN
	DBMS_OUTPUT.PUT_LINE('YES SPECIFIED CURSOR IS OPENED');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO THE SPECIFIED CURSOR IS NOT OPENED');
	END IF;
	C:=1;
	UPTO:='&UPTO';
	DBMS_OUTPUT.PUT_LINE('ENAME		ENO		ESAL');
	WHILE(C<=UPTO)
	LOOP
	C:=C+1;
	FETCH P2_CURSOR INTO F_ENO,F_ENAME,F_ESAL;
	DBMS_OUTPUT.PUT_LINE(F_ENAME||'		'||F_ENO||'		'||F_ESAL);
	END LOOP;
	CLOSE P2_CURSOR;
END;


	