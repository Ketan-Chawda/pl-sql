CREATE OR REPLACE FUNCTION PRIME(V1 IN NUMBER)
	RETURN NUMBER IS
 
	FR NUMBER(2);
	C NUMBER(2);
BEGIN
	FR:=1;
	C:=0;
	LOOP
	EXIT WHEN FR=V1-1;
	FR:=FR+1;
	IF(V1 MOD 2=1 AND V1 MOD FR=0) THEN
	C:=C+1;
	END IF;
	END LOOP;

	IF(C=0 AND V1 MOD 2=1 OR V1<4) THEN
	DBMS_OUTPUT.PUT_LINE('YES IT IS A PRIME NUMBER');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO IT IS NOT A PRIME NUMBER');
	END IF;
	RETURN 0;
EXCEPTION
	WHEN OTHERS THEN
	DBMS_OUTPUT.PUT_LINE('AN EXCEPTION IS RAISED IN PRIME FUNCTION');

END PRIME;