CREATE OR REPLACE FUNCTION EVENODD(VAL IN NUMBER)
	 RETURN NUMBER IS
BEGIN
	DBMS_OUTPUT.PUT_LINE('YOU HAVE CALLED A USER-DEFINED FUNCTION NAMED EVENODD');
	IF VAL MOD 2=1 THEN
	DBMS_OUTPUT.PUT_LINE('NO IT IS NOT A EVEN NUMBER');
	RETURN 1;
	ELSE
	DBMS_OUTPUT.PUT_LINE('YES IT IS EVEN NUMBER');
	RETURN 2;
	END IF;

EXCEPTION
	WHEN OTHERS THEN
	DBMS_OUTPUT.PUT_LINE('SORRY AN EXCEPTION IS RAISED');
	RETURN 0;
END EVENODD;
