DECLARE
	CURSOR P7_CURSOR
	IS SELECT CNO,SUM(QTYREQ) AS TOT,COUNT(PNO) AS TOTAL
	FROM SALES_ORDER
	GROUP BY CNO;
	
	F_CNO SALES_ORDER.CNO%TYPE;
	F_QTY NUMBER(4);
	F_PRODUCT NUMBER(2);
	
	U_CNO SALES_ORDER.CNO%TYPE;
BEGIN
	U_CNO:='&U_CNO';
	
	OPEN P7_CURSOR;
	LOOP
	FETCH P7_CURSOR INTO F_CNO,F_QTY,F_PRODUCT;
	EXIT WHEN P7_CURSOR%NOTFOUND;
	IF(F_CNO=U_CNO) THEN
	DBMS_OUTPUT.PUT_LINE('CLIENTNO==>'||F_CNO);
	DBMS_OUTPUT.PUT_LINE('TOTAL QTY PURCHASED==>'||F_QTY);
	DBMS_OUTPUT.PUT_LINE('TOTAL TYPE OF PRODUCT PURCHASED==>'||F_PRODUCT);
	END IF;
	END LOOP;
END;
	
	
	
