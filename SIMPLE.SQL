DECLARE
	CURSOR MYKETAN
	IS SELECT SNAME,RNO
	FROM STUDENT
	WHERE PERCENT>=70;
	
	F_SNAME STUDENT.SNAME%TYPE;
	F_RNO STUDENT.RNO%TYPE;
BEGIN
	OPEN MYKETAN;
	IF(MYKETAN%ISOPEN) THEN
	DBMS_OUTPUT.PUT_LINE('YES THE CURSOR IS OPENED');
	LOOP
	FETCH MYKETAN INTO F_SNAME,F_RNO;
	EXIT WHEN MYKETAN%NOTFOUND;
	DBMS_OUTPUT.PUT_LINE('SNAME='||F_SNAME||'	RNO='||F_RNO);
	DBMS_OUTPUT.PUT_LINE('ROWS FETCHED SO FAR IS='||MYKETAN%ROWCOUNT);
	END LOOP;
	DBMS_OUTPUT.PUT_LINE('TOTAL ROWS WERE FETCHED IS='||MYKETAN%ROWCOUNT);
	CLOSE MYKETAN;
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO,THE CURSOR IS OPENED');
	END IF;
	
END;

