DECLARE
	ID NUMBER(4);
BEGIN
	ID:='&ID';
	
	CASE 

	WHEN ID>=0 AND ID<10 THEN
	DBMS_OUTPUT.PUT_LINE('THE VALUE IS BETWEEN 0-10');

	WHEN ID>=10 AND ID<20THEN
	DBMS_OUTPUT.PUT_LINE('THE VALUE IS BETWEEN 10-20');

	ELSE
	DBMS_OUTPUT.PUT_LINE('THE VALUE IS ABOVE 20');
	END CASE;
END;
	