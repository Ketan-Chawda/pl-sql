DECLARE
	CURSOR P3_CURSOR
	IS SELECT ESAL FROM EMP
	ORDER BY ESAL;
	SALARY EMP.ESAL%TYPE;
	C NUMBER(2);
	UPTO NUMBER(2);
BEGIN
	OPEN P3_CURSOR;

	IF(P3_CURSOR%ISOPEN) THEN
	DBMS_OUTPUT.PUT_LINE('YES SPECIFIED CURSOR IS OPENED');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO THE SPECIFIED CURSOR IS NOT OPENED');
	END IF;
	C:=1;
	UPTO:='&UPTO';

	WHILE(C<=UPTO)
	LOOP
	C:=C+1;
	FETCH P3_CURSOR INTO SALARY;
	DBMS_OUTPUT.PUT_LINE(SALARY);
	END LOOP;
	CLOSE P3_CURSOR;
END;


	