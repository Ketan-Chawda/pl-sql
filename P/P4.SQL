DECLARE
	CURSOR P4_CURSOR
	IS SELECT EID,SALARY,HRA,DA,IT
	FROM EMP1;

	F_SAL EMP1.SALARY%TYPE;
	F_DA EMP1.DA%TYPE;
	F_HRA EMP1.HRA%TYPE;
	F_IT EMP1.IT%TYPE;
	V_NET EMP1.NET%TYPE;
	F_EID EMP1.EID%TYPE;
BEGIN
	OPEN P4_CURSOR;
	
	LOOP

	FETCH P4_CURSOR INTO F_EID,F_SAL,F_HRA,F_DA,F_IT;

	EXIT WHEN P4_CURSOR%NOTFOUND;

	IF(F_SAL>=50000) THEN
	V_NET:=F_SAL+F_DA*0.4+F_HRA*0.3+F_IT*.2;
	ELSIF(F_SAL>=30000 AND F_SAL<50000) THEN
	V_NET:=F_SAL+F_DA*.3+F_HRA*0.2+F_IT*.15;
	ELSE
	V_NET:=F_SAL+F_DA*0.15+F_HRA*0.1+F_IT*.05;
	END IF;

	UPDATE 
	EMP1
	SET NET=V_NET
	WHERE EID=F_EID;
	END LOOP;
END;



	