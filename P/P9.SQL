DECLARE
	CURSOR P9_CURSOR
	IS SELECT EMP_INFO.ENO,ENAME,
	LOC,SAL,EMP_MST.DESG,WORKHRS,DEPTS.DNO
	FROM EMP_INFO,DEPTS,EMP_MST,WRK_HR
	WHERE EMP_INFO.ENO=EMP_MST.ENO
	AND EMP_INFO.ENO=WRK_HR.ENO
	AND EMP_MST.DNO=DEPTS.DNO;
	
	
	F_ENO EMP_INFO.ENO%TYPE;
	F_ENAME EMP_INFO.ENAME%TYPE;
	F_LOC DEPTS.LOC%TYPE;
	F_SAL EMP_MST.SAL%TYPE;
	F_DESG EMP_MST.DESG%TYPE;
	F_WORKHRS WRK_HR.WORKHRS%TYPE;
	F_DNO DEPTS.DNO%TYPE;
	
	U_DNO DEPTS.DNO%TYPE;
BEGIN
	U_DNO:='&U_DNO';
	OPEN P9_CURSOR;
	DBMS_OUTPUT.PUT_LINE('ENO		ENAME	LOC		SAL		DESG		WORKHRS     DNO');
	LOOP

	FETCH P9_CURSOR INTO F_ENO,F_ENAME,
	F_LOC,F_SAL,F_DESG,F_WORKHRS,F_DNO;

	EXIT WHEN P9_CURSOR%NOTFOUND;	

	IF(U_DNO=F_DNO) THEN
	DBMS_OUTPUT.PUT_LINE(F_ENO||'		'||F_ENAME||'	'||F_LOC||'		'||F_SAL||'		'||F_DESG||'		'||F_WORKHRS||'	'||F_DNO);
	END IF;
	END LOOP;
END;


	
