DECLARE
	CURSOR P2_CURSOR
	IS SELECT ESAL FROM EMP
	ORDER BY ESAL DESC;

	SALARY EMP.ESAL%TYPE;
	C NUMBER(1);
BEGIN
	OPEN P2_CURSOR;
	C:=0;

	WHILE(C<3)
	LOOP
	C:=C+1;
	FETCH P2_CURSOR INTO SALARY;
	DBMS_OUTPUT.PUT_LINE('SALARY '||C||'='||SALARY);
	END LOOP;

	CLOSE P2_CURSOR;
END;


	