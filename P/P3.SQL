DECLARE 
	CURSOR P3_CURSOR
	IS SELECT SNAME,RNO,PERCENT
	FROM STUDENT;
	
	NAME STUDENT.SNAME%TYPE;
	ROLL STUDENT.RNO%TYPE;
	PER STUDENT.PERCENT%TYPE;
	GRADES RESULT.GRADE%TYPE;
BEGIN
	OPEN P3_CURSOR;
	LOOP
	FETCH P3_CURSOR INTO NAME,ROLL,PER;
	EXIT WHEN P3_CURSOR%NOTFOUND;

	IF(PER>=70) THEN
	GRADES:='DISTINCT';
	ELSIF(PER>=60) THEN
	GRADES:='FIRST';
	ELSIF(PER>=50) THEN
	GRADES:='SECOND';
	ELSIF(PER>=40) THEN
	GRADES:='PASS';
	ELSE
	GRADES:='FAIL';
	END IF;
	
	INSERT INTO RESULT
	VALUES(ROLL,NAME,GRADES);
	END LOOP;
END;
