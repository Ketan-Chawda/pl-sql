CREATE OR REPLACE TRIGGER T2

	BEFORE DELETE OR UPDATE OR INSERT
	ON TEMP1

BEGIN

	IF(DELETING OR UPDATING) THEN
	RAISE_APPLICATION_ERROR(-20012,'YOU CANNOT DELETE OR UPDATE');
	ELSIF(NEXT_DAY(SYSDATE-1,'THURSDAY')=SYSDATE AND INSERTING) THEN
	DBMS_OUTPUT.PUT_LINE('YOU CAN INSERT AS TODAY IS THURSDAY');
	ELSE
	RAISE_APPLICATION_ERROR(-20012,'YOU CANNOT INSERT AS TODAY IT IS NOT THURSDAY');
	END IF;
END T2;