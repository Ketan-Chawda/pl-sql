CREATE OR REPLACE TRIGGER T3

	BEFORE INSERT
	ON TEMP2
	FOR EACH ROW
DECLARE
	NO TEMP2.ENO%TYPE;
BEGIN
	IF(:NEW.ENO NOT LIKE 'E%') THEN
	RAISE_APPLICATION_ERROR(-20031,'IT SHOULD BE START WITH E ONLY');
	END IF;

	SELECT ENO INTO NO
	FROM TEMP2
	WHERE ENO=:NEW.ENO;

	IF(NO=:NEW.ENO) THEN
	RAISE_APPLICATION_ERROR(-20031,'IT SHOULD NOT BE REPEATED');
	END IF;
EXCEPTION
	WHEN NO_DATA_FOUND THEN
	DBMS_OUTPUT.PUT_LINE('IT IS UNIQUE ONLY OK');
END T3;
	
	
