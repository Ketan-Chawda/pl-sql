DECLARE
	CURSOR MYCURSOR
	IS SELECT ENO,ESAL
	FROM EMP
	WHERE DNO=2;
	
	EMPNO EMP.ENO%TYPE;
	SALARY EMP.ESAL%TYPE;
BEGIN
	OPEN  MYCURSOR;
	
	IF(MYCURSOR%ISOPEN) THEN
	LOOP

	FETCH MYCURSOR INTO EMPNO,SALARY;
	EXIT WHEN MYCURSOR%NOTFOUND;
	
	UPDATE EMP
	SET ESAL=ESAL+(ESAL*.05)
	WHERE EMPNO=ENO;
	
	END LOOP;
	COMMIT;
	CLOSE MYCURSOR;
	
	ELSE
	DBMS_OUTPUT.PUT_LINE('SORRY,UNABLE TO OPEN THE SPECIFIED CURSOR');
	END IF;
END;