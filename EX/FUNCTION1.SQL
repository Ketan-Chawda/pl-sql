CREATE OR REPLACE FUNCTION 
MYSTYLE(ANO IN ACCOUNT.ACNO%TYPE,TYPE IN CHAR,AMT IN ACCOUNT.BAL%TYPE)
	RETURN NUMBER IS
BEGIN
	IF(TYPE='C') THEN
	
	UPDATE ACCOUNT
	SET BAL=BAL+AMT
	WHERE ACNO=ANO;
	
	ELSIF(TYPE='D') THEN
		
	UPDATE ACCOUNT
	SET BAL=BAL-AMT
	WHERE ACNO=ANO;

	END IF;

	RETURN 12;
EXCEPTION
	WHEN NO_DATA_FOUND THEN
	RETURN 0;
	WHEN OTHERS THEN
	RETURN 0;
END MYSTYLE;

	
	
