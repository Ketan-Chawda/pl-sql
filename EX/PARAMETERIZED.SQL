DECLARE
	CURSOR KETAN_CURSOR(KNO NUMBER)
	IS SELECT ENO FROM EMP;

	GET NUMBER(5);
	STORE NUMBER(5);
BEGIN
	GET:='&GET';
	
	OPEN KETAN_CURSOR(GET);
	FETCH KETAN_CURSOR INTO STORE;
	
	IF(KETAN_CURSOR%FOUND) THEN
	DBMS_OUTPUT.PUT_LINE('YES, INPUTTED ENO IS FOUND');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO, THE INPUTTED ENO IS NOT FOUND');
	END IF;
	
	CLOSE KETAN_CURSOR;
END;
	