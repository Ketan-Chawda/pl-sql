DECLARE
	U_ACNO ACCOUNT.ACNO%TYPE;
	U_TYPE CHAR(1);
	U_AMT ACCOUNT.BAL%TYPE;
	RESULT NUMBER(2);
BEGIN
	U_ACNO:='&U_ACNO';
	U_TYPE:='&U_TYPE';
	U_AMT:='&U_AMT';
	RESULT:=MYSTYLE(U_ACNO,U_TYPE,U_AMT);
	IF(RESULT=12) THEN
	DBMS_OUTPUT.PUT_LINE('YES THE RECORD IS UPDATED');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NO THE RECORD IS NOT UPDATED');
	END IF;
END;

	